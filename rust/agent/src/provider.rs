//! Provider format dispatch.
//!
//! Only [`ProviderFormat::Anthropic`] exists for now, but keeping this as an
//! enum preserves the dispatch seam so additional inference providers can slot
//! in without churning the `to_provider_format` APIs on `ToolSchema` and
//! `Trajectory` (added in later milestones).

use serde::{Deserialize, Serialize};
use serde_json::{json, Value};

/// Wire format that a tool definition or trajectory is rendered into for a
/// given inference provider.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum ProviderFormat {
    Anthropic,
}

impl ProviderFormat {
    /// Render one tool definition into this provider's wire format.
    ///
    /// `params_schema` is the JSON Schema for the tool's model-supplied params
    /// (generated by `schemars`). This is the single place tool serialization
    /// per provider lives, so every tool gets every format for free.
    pub fn format_tool(self, name: &str, description: &str, params_schema: Value) -> Value {
        match self {
            ProviderFormat::Anthropic => json!({
                "name": name,
                "description": description,
                "input_schema": params_schema,
            }),
        }
    }
}
